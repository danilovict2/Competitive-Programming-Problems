const int maxN = 10000000;
bool prime[maxN + 1];

void sieve(int n){
    memset(prime, true, sizeof(prime));
 
    for (int p = 2; p * p <= n; p++) {
        if (prime[p] == true) {
            for (int i = p * p; i <= n; i += p)
                prime[i] = false;
        }
    }
}


void solve(){
	int curr = 0, ans = 0;
	for(int a=-999;a<1000;++a) {
		for(int b=-999;b<1000;++b) {
			int n = 0;
			while (prime[n*n + a*n + b]) {
				n++;
			}
			if (n > curr) {
				curr = n;
				ans = a*b;
			}
		}
	}

	write(ans,'\n');
}

