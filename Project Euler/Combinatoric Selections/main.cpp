void solve() {
    int ans = 0;
    for(int n=1;n<=100;++n) {
        for(int r=1;r<=n;++r) {
            long double prod = 1;
            for(long double i=n;i>max(n-r, r);--i)
                prod *= i;
            for (long double i=2;i<=min(n-r, r);++i)
                prod /= i;
            write(n,' ', r,' ', prod, '\n');
            if (prod > 1000000)ans++;
        }
    }

    write(ans, '\n');
}

