ll k;

void solve(){
	read(k);
	vector<int> dig;
	while(k){
		dig.pb(k%9);
		k/=9;
	}
	ll ans = 0;
	for(int i=dig.size()-1; i>=0; --i){
		ans *= 10;
		if(dig[i] >= 4 && dig[i] < 9)ans+=dig[i]+1;
		else ans+=dig[i];
	}
	write(ans,"\n");
}

