void solve(){
	string s;
	read(s);
	vi prefix(n);
	for(int i=0;i<n;++i){
		prefix[i] = s[i]-'a'+1;
		if(i)prefix[i]+=prefix[i-1];
	}
	int l,r;
	while(q--){
		read(l,r);
		int ans = prefix[r-1];
		ans-= (l-2>=0)?prefix[l-2]:0;
		write(ans,"\n");
	}
}



