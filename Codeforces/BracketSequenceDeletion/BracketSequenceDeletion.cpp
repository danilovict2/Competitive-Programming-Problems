int n;

void solve(){
	string s;
	read(n,s);
	int l = 0,cnt=0;	
	while(l+1 < n){
		if(s[l] == '(' || (s[l] == ')' && s[l+1] == ')'))
			l+=2;
		else{
			int r = l+1;
			while(r<n && s[r] != ')')
				++r;
			if(r == n)
				break;
			l = r+1;
		}
		++cnt;
	}
	write(cnt, " ", n-l, "\n");
}

