int n;


void solve(){
	int a[n];
	int ans, cnt=0;
	for(int i=0;i<n;++i)read(a[i]);
	for(int i=0;i<n-1;++i)if(a[i] > a[i+1])ans = i,++cnt;
	if(a[n-1] > a[0])ans=n-1,++cnt;
	if(cnt == 0)write("0\n");
	else if(cnt > 1)write("-1\n");
	else write(n-ans-1,"\n");
}


